/* FIPS 197 Appendix A.3: first round-key word of the expansion for the
 * AES-256 example key (same key as used in A.3).
 * key:
 *   60 3d eb 10 15 ca 71 be 2b 73 ae f0 85 7d 77 81
 *   1f 35 2c 07 3b 61 08 d7 2d 98 10 a3 09 14 df f4
 * expected round_keys[56..59] (i=14, final round key word 0):
 *   fe 48 90 d1
 */
static const uint8_t fips197_a3_final_word[4] = { 0xFE, 0x48, 0x90, 0xD1 };

int main(void) {
    uint8_t round_keys[240];
    uint8_t ct[16];

    printf("===========================================\n");
    printf("AES-256 Constant-Time Bitsliced Test Suite\n");
    printf("===========================================\n\n");

    /* Test 1: full S-box reference table match. */
    int mismatches = 0;
    for (int i = 0; i < 256; i++) {
        uint8_t got = ama_aes_sbox_consttime((uint8_t)i);
        if (got != fips197_sbox[i]) mismatches++;
    }
    TEST_ASSERT(mismatches == 0, "ama_aes_sbox_consttime matches FIPS 197 S-box for all 256 inputs");

    /* Test 2: key expansion — round_keys[56..59] matches FIPS 197 A.3 example. */
    ama_aes256_key_expansion_consttime(kat_key, round_keys);
    TEST_ASSERT(memcmp(round_keys + 56 * 4, fips197_a3_final_word, 4) == 0,
                "key schedule final word matches FIPS 197 Appendix A.3");

    /* Test 3: NIST SP 800-38A §F.1.5 AES-256 ECB — all four blocks. */
    ama_aes256_encrypt_block_consttime(round_keys, kat_pt1, ct);
    TEST_ASSERT(memcmp(ct, kat_ct1, 16) == 0, "AES-256 ECB block 1 matches NIST F.1.5");

    ama_aes256_encrypt_block_consttime(round_keys, kat_pt2, ct);
    TEST_ASSERT(memcmp(ct, kat_ct2, 16) == 0, "AES-256 ECB block 2 matches NIST F.1.5");

    ama_aes256_encrypt_block_consttime(round_keys, kat_pt3, ct);
    TEST_ASSERT(memcmp(ct, kat_ct3, 16) == 0, "AES-256 ECB block 3 matches NIST F.1.5");

    ama_aes256_encrypt_block_consttime(round_keys, kat_pt4, ct);
    TEST_ASSERT(memcmp(ct, kat_ct4, 16) == 0, "AES-256 ECB block 4 matches NIST F.1.5");

    printf("\n===========================================\n");
    printf("All AES-256 bitsliced tests passed ✓\n");
    printf("===========================================\n");
    return 0;
}
